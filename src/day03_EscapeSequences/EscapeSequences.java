package day03_EscapeSequences;
/*
Escape Sequences: Must be given with double quote
\n: stars a new line
\t: paragraf space(tab)
\\: single back slash
\": double quote

 */
public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Java \nPyhton \nC#");

        System.out.println("--------------------");

        System.out.println("Hello Cydeo. \nHow are you all today? \nIt is nice to see you all. \nWhat class do we have next week");

        System.out.println("-----------------------------");

        System.out.println("\tJava is cool programming");

        System.out.println("----------------------");

        System.out.println("/ \\"); // in order to print one backward slash we need to give two backward slashes

        System.out.println("----------------------");

        System.out.println("My favorite TV show is \"Game of Throne\"");



    }
}
